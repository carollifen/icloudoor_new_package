package com.icloudoor.cloudoor.chat.entity;

public class Key {

	private String authTo;
	private String doorType;
	private String authFrom;
	private String authTime;
	private String authStatus;
	private String useStatus;
	private String name;
	public String getAuthTo() {
		return authTo;
	}
	public void setAuthTo(String authTo) {
		this.authTo = authTo;
	}
	public String getDoorType() {
		return doorType;
	}
	public void setDoorType(String doorType) {
		this.doorType = doorType;
	}
	public String getAuthFrom() {
		return authFrom;
	}
	public void setAuthFrom(String authFrom) {
		this.authFrom = authFrom;
	}
	public String getAuthTime() {
		return authTime;
	}
	public void setAuthTime(String authTime) {
		this.authTime = authTime;
	}
	public String getAuthStatus() {
		return authStatus;
	}
	public void setAuthStatus(String authStatus) {
		this.authStatus = authStatus;
	}
	public String getUseStatus() {
		return useStatus;
	}
	public void setUseStatus(String useStatus) {
		this.useStatus = useStatus;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
